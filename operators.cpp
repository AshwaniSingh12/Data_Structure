#include<iostream>
using namespace std;
int main()
{

    // relational operators 
    int a=2;
    int b=3;
    bool first = (a==b);
    cout<< first<<endl;
    bool second = (a>=b);
    cout<< second<<endl;
    bool third = (a<=b);
    cout<< third<<endl;
    bool fourth = (a!=b);
    cout<< fourth<<endl;

    // logical operators are used for conditional statement

    int m=0;
    cout<< !m<<endl;
}